#----- Assigning treatment names ------------ 
########################################################################################

valve_data <- dat %>%
  mutate(treatment = recode(id,
                            `1` = 'cont',
                            `2` = 'bkg',
                            `3` = 'acid',
                            `4` = 'cont',
                            `5` = 'bkg',
                            `6` = 'acid',
                            `7` = 'cont',
                            `8` = 'bkg',
                            `9` = 'acid',
  ),
  group = case_when(
    id %in% c(1, 4, 7) ~ 'control',
    id %in% c(2, 5, 8) ~ 'background',
    id %in% c(3, 6, 9) ~ 'acid treatment',
  )
  )
dat <- rbind(valve_data)

